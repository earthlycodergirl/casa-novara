function translateAdv($val){
  global $link_contact;
  global $lang;
  switch($val){
    case 'city':
      $vald = 'city';
      $vals = 'cities';
      break;
    case 'area':
      $vald = 'area';
      $vals = 'areas';
      break;
    case 'town':
      $vald = 'town';
      $vals = 'towns';
      break;
    case 'zip':
      $vald = 'zip code';
      $vals = 'zip codes';
      break;
  }
  return array(
    strtoupper($vald).' SEARCH',
    'Please select all the '.$vals.' you wish to search',
    'show all '.$vals,
    'No '.$vals.' found',
    'We apologize for the inconvenience, but we do not have any properties available online. Please <a href="'.$link_contact[$lang].'">contact us</a> now for a full list of properties in this destination.',
    'This is a list of all the '.$vals.' where we have properties listed. Including the number of properties in parenthesis next to each '.$vald.'. If you do not see the '.$vald.' you are looking for, please <a href="'. $link_contact[$lang].'">contact us directly</a> to find exactly what you are looking for.'
  );
}
